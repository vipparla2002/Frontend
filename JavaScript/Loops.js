function forAndWhile()
{
    for(let i = 1;i<20;i++)
    {
        if(i%2==0)
        {
            console.log(i,i**2);
        }
    }
    j = 1;
    while(j<30)
    {
        if(j%2!=0)
        {
            console.log(j,j**3);
        }
        j++
    }
}


forAndWhile();

